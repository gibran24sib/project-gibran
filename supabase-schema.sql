-- Supabase schema untuk aplikasi Klinik Gigi
-- Jalankan file ini di Supabase Dashboard > SQL Editor.

create table if not exists users (
    id bigint generated by default as identity primary key,
    username text not null unique,
    password_hash text,
    full_name text not null,
    email text unique,
    role text not null default 'admin',
    is_active boolean not null default true,
    created_at timestamptz not null default now()
);

create table if not exists patients (
    id bigint generated by default as identity primary key,
    rm_number text unique,
    full_name text not null,
    nik text,
    phone text,
    whatsapp text,
    email text,
    gender text,
    birth_place text,
    birth_date date,
    address text,
    allergies text,
    systemic_diseases text,
    special_notes text,
    is_active boolean not null default true,
    created_at timestamptz not null default now()
);

create table if not exists patient_documents (
    id bigint generated by default as identity primary key,
    patient_id bigint references patients(id) on delete cascade,
    document_type text,
    file_name text,
    file_url text,
    notes text,
    created_at timestamptz not null default now()
);

create table if not exists doctors (
    id bigint generated by default as identity primary key,
    user_id bigint references users(id) on delete set null,
    specialization text,
    license_number text,
    phone text,
    is_active boolean not null default true,
    created_at timestamptz not null default now()
);

create table if not exists doctor_schedules (
    id bigint generated by default as identity primary key,
    doctor_id bigint references doctors(id) on delete cascade,
    day_of_week text not null,
    start_time time,
    end_time time,
    is_active boolean not null default true,
    created_at timestamptz not null default now()
);

create table if not exists treatments (
    id bigint generated by default as identity primary key,
    code text unique,
    name text not null,
    category text,
    price numeric not null default 0,
    duration_minutes integer,
    description text,
    is_active boolean not null default true,
    created_at timestamptz not null default now()
);

create table if not exists treatment_packages (
    id bigint generated by default as identity primary key,
    name text not null,
    description text,
    price numeric not null default 0,
    is_active boolean not null default true,
    created_at timestamptz not null default now()
);

create table if not exists package_items (
    id bigint generated by default as identity primary key,
    package_id bigint references treatment_packages(id) on delete cascade,
    treatment_id bigint references treatments(id) on delete cascade,
    quantity integer not null default 1
);

create table if not exists appointments (
    id bigint generated by default as identity primary key,
    patient_id bigint references patients(id) on delete set null,
    doctor_id bigint,
    appointment_date date not null,
    appointment_time time,
    status text not null default 'pending',
    notes text,
    cancellation_reason text,
    cancelled_by bigint,
    created_at timestamptz not null default now()
);

create table if not exists appointment_treatments (
    id bigint generated by default as identity primary key,
    appointment_id bigint references appointments(id) on delete cascade,
    treatment_id bigint references treatments(id) on delete set null,
    quantity integer not null default 1,
    price numeric not null default 0,
    created_at timestamptz not null default now()
);

create table if not exists invoices (
    id bigint generated by default as identity primary key,
    invoice_number text unique,
    appointment_id bigint references appointments(id) on delete set null,
    patient_id bigint references patients(id) on delete set null,
    total_amount numeric not null default 0,
    discount_amount numeric not null default 0,
    final_amount numeric not null default 0,
    status text not null default 'unpaid',
    created_at timestamptz not null default now()
);

create table if not exists payments (
    id bigint generated by default as identity primary key,
    invoice_id bigint references invoices(id) on delete cascade,
    payment_date date not null default current_date,
    amount numeric not null default 0,
    method text,
    notes text,
    created_at timestamptz not null default now()
);

create table if not exists inventory (
    id bigint generated by default as identity primary key,
    code text unique,
    name text not null,
    category text,
    stock integer not null default 0,
    min_stock integer not null default 0,
    unit text,
    price numeric not null default 0,
    expired_date date,
    created_at timestamptz not null default now()
);

create table if not exists inventory_transactions (
    id bigint generated by default as identity primary key,
    inventory_id bigint references inventory(id) on delete cascade,
    transaction_type text not null,
    quantity integer not null default 0,
    notes text,
    created_at timestamptz not null default now()
);

create table if not exists surveys (
    id bigint generated by default as identity primary key,
    appointment_id bigint references appointments(id) on delete set null,
    patient_id bigint references patients(id) on delete set null,
    doctor_id bigint,
    rating integer,
    comment text,
    follow_up_status text default 'pending',
    created_at timestamptz not null default now()
);

create table if not exists loyalty_points (
    id bigint generated by default as identity primary key,
    patient_id bigint references patients(id) on delete cascade,
    points integer not null default 0,
    points_type text not null,
    source_activity text,
    reward_id bigint,
    created_at timestamptz not null default now()
);

create table if not exists rewards (
    id bigint generated by default as identity primary key,
    name text not null,
    points_required integer not null default 0,
    stock integer not null default 0,
    is_active boolean not null default true,
    created_at timestamptz not null default now()
);

create table if not exists reward_redemptions (
    id bigint generated by default as identity primary key,
    patient_id bigint references patients(id) on delete cascade,
    reward_id bigint references rewards(id) on delete set null,
    status text not null default 'pending',
    created_at timestamptz not null default now()
);

create table if not exists promotions (
    id bigint generated by default as identity primary key,
    name text not null,
    coupon_code text unique,
    discount_type text not null default 'fixed',
    discount_value numeric not null default 0,
    start_date date,
    end_date date,
    is_active boolean not null default true,
    created_at timestamptz not null default now()
);

create table if not exists communication_logs (
    id bigint generated by default as identity primary key,
    patient_id bigint references patients(id) on delete set null,
    channel text,
    message_type text,
    direction text,
    status text,
    content text,
    created_at timestamptz not null default now()
);

create table if not exists activity_logs (
    id bigint generated by default as identity primary key,
    user_id bigint references users(id) on delete set null,
    action text not null,
    table_name text,
    record_id bigint,
    old_data jsonb,
    new_data jsonb,
    ip_address text,
    created_at timestamptz not null default now()
);

create index if not exists idx_patients_full_name on patients(full_name);
create index if not exists idx_appointments_date on appointments(appointment_date);
create index if not exists idx_appointments_patient_id on appointments(patient_id);
create index if not exists idx_invoices_patient_id on invoices(patient_id);
create index if not exists idx_loyalty_points_patient_id on loyalty_points(patient_id);

